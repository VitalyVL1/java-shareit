package ru.practicum.shareit.booking.model;

/**
 * Перечисление возможных статусов бронирования в приложении ShareIt.
 * <p>
 * Определяет текущее состояние бронирования на протяжении его жизненного цикла.
 * Используется в сущности {@link Booking} и DTO для отображения и фильтрации бронирований.
 * </p>
 *
 * <p><b>Значения перечисления:</b></p>
 * <ul>
 *   <li>{@link #WAITING} - бронирование ожидает подтверждения владельца вещи
 *       (начальное состояние при создании запроса)</li>
 *   <li>{@link #APPROVED} - бронирование подтверждено владельцем вещи,
 *       аренда может состояться</li>
 *   <li>{@link #REJECTED} - бронирование отклонено владельцем вещи,
 *       аренда не состоится</li>
 *   <li>{@link #CANCELED} - бронирование отменено создателем (арендатором)
 *       после создания, но до подтверждения</li>
 * </ul>
 *
 * @see Booking
 * @see ru.practicum.shareit.booking.dto.State
 */
public enum Status {
    WAITING,
    APPROVED,
    REJECTED,
    CANCELED
}